int sum(int i, int j){
   return i + j;
}

int sub(int i, int j){
   return i - j;
}

float div(int i, int j){
   return i / j;
}

int mul(int i, int j){
   return i * j;
}

int mod(int i, int j){
   return i % j;
}

int square(int i){
   return i * i;
}

int cube(int i){
   return i * i * i;
}

int max1(int i, int j){
   if(i > j ){
        return i;
   }else{
        return j;
   }
}

int min1(int i, int j){
   if(i > j ){
        return j;
   }else{
        return i;
   }
}

