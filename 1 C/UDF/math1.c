#include<stdio.h>
#include<math.h>

void main(){
    printf("\n abs is %lf ", fabs(-12.6));
    printf("\n ceil is %lf", ceil(12.6));
    printf("\n floor is %lf", floor(12.6));
    printf("\n pow is %lf", pow(2, 16));
    printf("\n sin is %lf", sin(0.5));
    printf("\n fmod is %lf", fmod(12, 5));
    printf("\n sqrt is %lf", sqrt(144));
    printf("\n exp is %lf", exp(1));
}
